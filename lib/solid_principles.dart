// Single Responsibility Principle (SRP)
// A class should have only one reason to change


//  Open Closed Principle (OCP)
// The class should be open for extension but closed for modification


// Liskov Substitution Principle (LSP)
// If you substitute any type with one of it's subtypes, the behavior should not change

// Interface Segregation Principle (ISP)
// Avoid making general interface contains all methods.

//Dependency Inversion Principle (DIP)
// High-level classes should not know the implementation of low-level classes but depends on abstractions

